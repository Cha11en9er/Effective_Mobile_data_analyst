import time
import tracemalloc
import math

"""
Решение через последовательное пробное деление.
Делим n на каждый делитель начиная с 2.
Если делитель делит n — добавляем его в результат и продолжаем делить.
Проверяем делители только до sqrt(n): если после всех делений n > 1,
то оставшееся n само является простым множителем.
"""
def prime_factors_v1(n: int) -> list[int]:

    factors = []
    d = 2
    while d * d <= n:
        while n % d == 0:
            factors.append(d)
            n //= d
        d += 1
    if n > 1:
        factors.append(n)
    return factors


"""
Оптимизация: сначала выбираем все двойки отдельно,
затем проверяем только нечётные делители 3, 5, 7, ...
Это вдвое сокращает количество итераций по сравнению с v1,
так как все чётные числа кроме 2 не могут быть простыми.
"""
def prime_factors_v2(n: int) -> list[int]:

    factors = []

    while n % 2 == 0:
        factors.append(2)
        n //= 2

    d = 3
    while d * d <= n:
        while n % d == 0:
            factors.append(d)
            n //= d
        d += 2

    if n > 1:
        factors.append(n)

    return factors


"""
Вспомогательная функция для замера
"""

def measure(func, n: int, label: str):
    tracemalloc.start()
    start = time.perf_counter()

    result = func(n)

    end = time.perf_counter()
    current, peak = tracemalloc.get_traced_memory()
    tracemalloc.stop()

    print(f"  [{label}]  Результат: {result}")
    print(f"           Время:     {(end - start) * 1_000_000:.3f} мкс")
    print(f"           Память:    {peak} байт (пик)\n")


examples = [
    (56,          [2, 2, 2, 7]),          # пример из задания
    (360,         [2, 2, 2, 3, 3, 5]),    # много множителей
    (999999937,   [999999937]),            # большое простое число
]

for i, (n, expected) in enumerate(examples, 1):
    print(f"Пример {i}: n={n}")
    print(f"          Ожидается: {expected}")
    measure(prime_factors_v1, n, "Решение 1 (все делители)      ")  
    measure(prime_factors_v2, n, "Решение 2 (2 + нечётные)      ")

